public class Serpiente {
    private int posicionAlta;
    private int posicionBaja;

    public Serpiente(int posicionAlta, int posicionBaja) {
        this.posicionAlta = posicionAlta;
        this.posicionBaja = posicionBaja;
    }

    public int getPosicionAlta() {
        return posicionAlta;
    }

    public int getPosicionBaja() {
        return posicionBaja;
    }
}
